    public void totalAsignadoEnPrimas(){
        for (int i = 0; i < listaEmpleados.size(); i++) {
            totAsignado=totAsignado+listaEmpleados.get(i).getPrima();
        }
    }
    public Empleado conMayorPrima(){
        for (int i = 0; i < listaEmpleados.size(); i++) {
            if(mayor<listaEmpleados.get(i).getPrima()){
                mayor=listaEmpleados.get(i).getPrima();
                conMayor=listaEmpleados.get(i);
        }
    }     
        return conMayor;
    }
    public String conMenorPrima(){
     String nombree=null;    
     for (int i = 0; i < listaEmpleados.size(); i++) {
            if(listaEmpleados.get(i).getPrima()<=menor){
                menor=listaEmpleados.get(i).getPrima();
                nombree=listaEmpleados.get(i).getNombre();
        }
    }       
    return nombree;
        }
    public boolean saberSiTendraPrima(String tipoEmpleado){
    boolean saberSiTendraPrima;
        if(tipoEmpleado.equals("OTRO")){
    sinPrima++;
    JOptionPane.showMessageDialog(null,"Este empleado no tiene derecho a prima");
    saberSiTendraPrima=false;
}
else{
    conPrima++;
    saberSiTendraPrima=true;
}
        return saberSiTendraPrima;
    }
    public void asignarSueldo(String tipoEmpleado){
    if(tipoEmpleado.equals("DOCENTE")){
        sueldo=3700000;
    }
    if(tipoEmpleado.equals("ADMINISTRATIVO")){
        sueldo=4000000;
    }
    if("CONTRATISTA".equals(tipoEmpleado)){
        sueldo=3500000;
    }
}
    public void calcularPrima(String tipoEmpleado, int sueldo){
    if("DOCENTE".equals(tipoEmpleado)){
        prima=(int) (sueldo*0.35);
    }
    if("CONTRATISTA".equals(tipoEmpleado)){
        prima=(int)(sueldo*0.12);
    }
    if("ADMINISTRATIVO".equals(tipoEmpleado)){
        prima=(int)(sueldo*24);
    }
}
