    private void guardarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBotonActionPerformed
nombre=nombreTexto.getText();
identidad=identificacionTexto.getText();
tipoEmpleado=(String) tipoEmpleadosCombo.getSelectedItem();
asignarSueldo(tipoEmpleado);
saberSiTendraPrima(tipoEmpleado);
if(saberSiTendraPrima(tipoEmpleado)==true){
calcularPrima(tipoEmpleado, sueldo);
listaEmpleados.add(new Empleado(nombre,identidad,sueldo,prima,tipoEmpleado));
conMayorPrima();
conMenorPrima();
totalAsignadoEnPrimas();
if(tipoEmpleado.equals("CONTRATISTA")){
    contratistaCombo.addItem(identidad+" "+nombre+" "+tipoEmpleado+" "+sueldo+" "+prima);
}   
}
    }//GEN-LAST:event_guardarBotonActionPerformed

